    /* The accelerometer due to rotation action */
    private TextView mXAccelView, mYAccelView, mZAccelView;

    /* The accelerometer due including the gravity of the earth */
    private TextView mXGravityView, mYGravityView, mZGravityView;

    /* Arrays for storing filtered value */
    private float[] gravity = new float[3];
    private float[] accel = new float[3];
    private double accel_total;

    private long mLastUpdate;

    @Override
    public void onSensorChanged(SensorEvent event) {

		/* acquire accelerometer event data */
        if (event.sensor.getType() == Sensor.TYPE_ACCELEROMETER) {
            mGravity = new float[3];
            System.arraycopy(event.values, 0, mGravity, 0, 3);

            long actualTime = System.currentTimeMillis();

            if (actualTime - mLastUpdate > 500) {
                mLastUpdate = actualTime;

                float rawX = event.values[0];
                float rawY = event.values[1];
                float rawZ = event.values[2];

				/* apply low-pass filter */
                gravity[0] = lowPass(rawX, gravity[0]);
                gravity[1] = lowPass(rawY, gravity[1]);
                gravity[2] = lowPass(rawZ, gravity[2]);

				/* apply high-pass filter */
                accel[0] = highPass(rawX, gravity[0]);
                accel[1] = highPass(rawY, gravity[1]);
                accel[2] = highPass(rawZ, gravity[2]);

                accel_total = Math.sqrt(accel[0] * accel[0] + accel[1] * accel[1] + accel[2] * accel[2]);
                writeStringToTxt(String.valueOf(accel_total));
                mAcceleration.setText(String.valueOf("Accleration: " + accel_total));
                if((accel_total>8.5) && (!mIsRecordingVideo)){
                    Log.d("Video","Auto-Videoing");
                    takeVideo();
                }
            }
        }
        /* acquire magnetometer event data */
        else if (event.sensor.getType() == Sensor.TYPE_MAGNETIC_FIELD) {
            mGeomagnetic = new float[3];
            System.arraycopy(event.values, 0, mGeomagnetic, 0, 3);
        }

		/* If we have readings from both sensors then use readings to compute
		 * the device's orientation and then update the display */
        if (mGravity != null && mGeomagnetic!=null ) {
            float rotationMatrix[] = new float[9];

			/* users the accelerometer and magnetometer readings to compute
			 * the device's roatation with respect to a real world coordinate
			 * system */

            boolean success = SensorManager.getRotationMatrix(rotationMatrix, null,
                    mGravity, mGeomagnetic);

            if (success) {
                float orientationMatrix[] = new float[3];

				/* return device's orientation given the roationMatrix */
                SensorManager.getOrientation(rotationMatrix, orientationMatrix);

				/* get the rotation, measured in radians, around the Z-axis */
                float rotationInRadians = orientationMatrix[0];

				/* convert from radians to degrees */
                mRotationInDegree = Math.toDegrees(rotationInRadians);

//				/* request redraw */
//                mDraw.invalidate();

				/* reset the sensor event data arrays */
                mGravity = mGeomagnetic = null;
            }
        }
    }

    private float lowPass(float current, float gravity) {
        return gravity * mAlpha + current * (1 - mAlpha);
    }

    private float highPass(float current, float gravity) {
        return current - gravity;
    }

